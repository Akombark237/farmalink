// src/app/api/chat-history/[sessionId]/route.js
// Proxy API for chat history management

import { NextResponse } from 'next/server';

const GEMINI_BACKEND_URL = process.env.GEMINI_BACKEND_URL || 'http://localhost:3001';

export async function GET(request, { params }) {
  try {
    const { sessionId } = params;

    if (!sessionId) {
      return NextResponse.json(
        { error: 'Session ID is required' },
        { status: 400 }
      );
    }

    // Forward the request to the Gemini backend
    const response = await fetch(`${GEMINI_BACKEND_URL}/api/chat-history/${sessionId}`, {
      method: 'GET',
      headers: {
        'Content-Type': 'application/json',
      },
    });

    if (!response.ok) {
      const errorData = await response.json().catch(() => ({}));
      return NextResponse.json(
        { 
          error: 'Failed to fetch conversation history',
          details: errorData.error || 'Unknown error'
        },
        { status: response.status }
      );
    }

    const data = await response.json();
    return NextResponse.json(data);

  } catch (error) {
    console.error('Error fetching chat history:', error);
    return NextResponse.json(
      { 
        error: 'Internal server error',
        details: error.message 
      },
      { status: 500 }
    );
  }
}

